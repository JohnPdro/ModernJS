// Math object

console.log(Math);
console.log(Math.PI);
console.log(Math.E);

const area = 7.7;

console.log(Math.round(area)); // Arredonda o número
console.log(Math.floor(area)); // Sempre arredonda para baixo
console.log(Math.ceil(area)); // Sempre arredonda para cima
console.log(Math.trunc(area)); // Retorna o número inteiro removendo digitos fracionais

// random numbers

const random = Math.random(); // Mostra um número aleatorio entre 0 e 1 
console.log(random);
console.log(Math.round(random * 100)); // Mostra um número aleatório entre 0 e 100


